class Mapa():
    def __init__(self, game, rows):
        self.game = game
        self.rows = rows
        self.width_muro = game.GAME_H // rows
        self.columns = game.GAME_W // self.width_muro
        self.width_coin = game.GAME_H // rows
        self.columns = game.GAME_W // self.width_coin
        self.width_garden = game.GAME_H // rows
        self.columns = game.GAME_W // self.width_garden
        self.map = self.make_map()
        self.make_map01()

    def make_map(self):
        map = []
        for i in range(self.rows):
            map.append([])
            for j in range(self.columns):
                cell = Cell(j, i, self.width_muro, self.rows)
                map[i].append(cell)
                if i == 0 or j == 0 or i == self.rows - 1 or j == self.columns - 1:
                    cell.make_muro()
                    cell.set_is_muro(True)
        return map

