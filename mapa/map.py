    def make_map_with_grid(self, grid):
        for i in range(self.rows):
            for j in range(self.columns):
                if grid[i][j] == 1:
                    self.map[i][j].make_muro()
                    self.map[i][j].set_is_muro(True)
                if grid[i][j] == 2:
                    self.map[i][j].make_coin()
                    self.map[i][j].set_is_coin(True)
                if grid[i][j] == 0:
                    self.map[i][j].make_garden()
                    self.map[i][j].set_is_garden(True)

    def draw_grid(self, win):
        gap = self.width_muro
        for i in range(self.rows):
            pygame.draw.line(win, GREY, (0, i * gap), (self.game.GAME_W, i * gap))
            for j in range(self.columns):
                pygame.draw.line(win, GREY, (j * gap, 0), (j * gap, self.game.GAME_H))

    def draw(self, win):
        for row in self.map:
            for spot in row:
                if spot.get_is_muro():
                    spot.draw_muro(win)
                if spot.get_is_coin():
                    spot.draw_coin(win)
                if spot.get_is_garden():
                    spot.draw_garden(win)
                else:
                    continue
        #self.draw_grid(win)
        pygame.display.update()

